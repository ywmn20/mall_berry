<template>
  <div class="tab-control">
      <div v-for="(item,index) in titles" class="tab-control-item"
      :class="{active:index===curIndex}" @click="itemClick(index)"
      >
          <span>{{item}}</span>
      </div>
  </div>
</template>

<script>
export default {
    data(){
        return {
            curIndex:0
        }
    },    
    props:{
        titles:{
            type:Array,
            default(){
                return []
            }
        }
    },
    methods:{
        itemClick(index){
            this.curIndex=index;
            this.$emit('tabClick',index)
        }
    }
}
</script>

